void	ft_rx(int *stk, int tam)
{
	int	tmp;
	int	i;

	tmp = stk[0];
	i = 0;
	while (i < (tam - 1))
	{
		stk[i] = stk[i + 1];
		i++;
	}
	stk[i] = tmp;
}
